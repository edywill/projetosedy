					<p><strong>INTRODUÇÃO</strong><br>
					  Em estudo realizado com base nos dados fornecidos pela CGRH verificamos que cerca de 300 Engenheiros do PEC se aposentarão compulsoriamente até 2016, daqui a 5 anos.<br>
					  A aposentadoria envolve um processo complexo, com extensa legislação (Anexo I), e o enquadramento de cada servidor nos fundamentos legais são determinantes no valor dos proventos do aposentado (Anexo III).<br>
					  Enquanto Associação, a AEDNIT tem o dever estatutário de defender os interesses de seus associados, prestando o apoio jurídico de modo a assegurar que os critérios adotados para a aposentadoria sejam os melhores possíveis dentro da legislação vigente.<br>
					  Usualmente, o servidor solicita aposentadoria e o Serviço de Pessoal forma um processo nas SRs ou na Sede. O processo é encaminhado à CGRH que analisa o processo e cadastra a aposentadoria no SIAPE.<br>
					  Após a oficialização da aposentadoria, o processo é homologado pela CGU, que inúmeras vezes determina revisões no enquadramento, freqüentemente beneficiando o servidor.<br>
				    O servidor a ser aposentado poderá ser enquadrado em nada menos de 359 diferentes códigos de fundamentos legais (Anexo 3), o que demonstra a complexidade do processo.</p>
					<p><strong>PROGRAMA PROPOSTO</strong><br>
					  1.	O associado solicitaria ao DNIT averbação de seus tempos de serviço anteriores ao ingresso no DNIT.<br>
					  2.	O associado solicita á CGRH certidão de tempo de serviço<br>
					  3.	O associado solicita ao DNIT simulação de sua aposentadoria,<br>
					  4.	O associado poderá fazer sua própria simulação no simulador da CGU -<a href="http://www.cgu.gov.br/Simulador/Legislacao.asp">http://www.cgu.gov.br/Simulador/Legislacao.asp</a> - e no simulador do SIAPENET<br>
					  5.	A simulação seria analisada por advogados da área previdenciária de um escritório de advocacia (a maioria dos grandes bancas de advogados dispõe de área previdenciária)<br>
					  6.	Caso o enquadramento possa ser alterado, beneficiando o servidor, a simulação será contestada, até se alcançar a melhor situação para o associado a ser aposentado.<br>
					  7.	A AEDNIT disponibilizaria em seu site, toda a legislação, simuladores e manuais sobre aposentadoria.<br>
					  8.	A AEDNIT disponibilizaria um canal de consulta, por email, para esclarecer seus associados sobre questões de aposentadoria, uso de simuladores, etc.<br>
					  9.	A AEDNIT promoveria palestras de esclarecimento aos servidores.</p>
					<p><strong>RESTRIÇÕES A SUPERAR</strong><br>
					  A CGRH dispõe de apenas três servidores capacitados para analisar todos os processos de aposentadoria do DNIT, não tendo capacidade para atender aos pedidos de simulações, sendo necessário, portanto negociar com a Administração a alocação de servidores para esta tarefa.<br>
					  Contratação do escritório de advocacia – a AEDNIT disporia dos recursos necessários quando atingisse os 500 associados e fosse autorizado desconto em folha. Temporariamente poderia ser negociado com outras entidades que mantém contratos de assessoria jurídica o atendimento de seus associados.</p>
					<p><strong>COORDENAÇÃO DO PROGRAMA</strong><br>
					  Seriam indicados três associados para coordenar o programa</p>
